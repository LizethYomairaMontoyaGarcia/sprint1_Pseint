// solicitar 2 numeros e indicar cual es mayo o iguales
let numero1 = parseInt (window.prompt ("ingresa el primer numero"));
let numero2 = parseInt (window.prompt ("ingresa el segundo numero"));

 if (numero1 < numero2) {
    console.log ("el numero  "  + numero1 + " es menor que " + numero2);
 } else if (numero1 > numero2) {
    console.log ("el numero  "  + numero1 + " es mayor que " + numero2);
 } else if (numero1 == numero2) {
    console.log ("el numero  "  + numero1 + " es igual a " + numero2);
 } 
 

